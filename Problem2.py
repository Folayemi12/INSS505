# calculating the area of a circle
# importing math module
import math
radius= float(input("Enter the value for radius:"))
# math.pi is a mathematical function to ascertain the value for pi
pi = input(math.pi)
# formula-Area_of_a_circle=pi*radius**2
Area_of_a_circle = (math.pi*radius**2)
print("Area_of_a_circle=", Area_of_a_circle)

